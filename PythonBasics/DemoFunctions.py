# function is a group of statements that perform a specific task

# function declaration
def GreetMe():
    print("Hello")

# function call
GreetMe()

# parameterized function declaration
def GreetMeAgain(name):
    print("Hello " +name)

# function call
GreetMeAgain("Yashu")


# function for summing two numbers

def SumOfTwo(a, b):
    return a + b


print(SumOfTwo(2, 4))

